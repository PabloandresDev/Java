package FlujosDeControl;

public class IfElse {
    public static void main(String[] args) {
        float promedio = 4.6f;
        if (promedio>4.4){
            System.out.println("Buen promedio");
        }else if (promedio >4.5){
            System.out.println("Excelente promedio");
        }else {
            System.out.println("Tienes que mejorar el promedio");
        }
    }
}
